const eqArrays = function(first, second) {
  let result;
  for (let i = 0; i < first.length; i++) {
    for (let j = 0; j < second.length; j++) {
      if (first[i] === second[j]) {
        result = true;
      } else {
        result = false;
      }
    }
  }
  return result;
};

