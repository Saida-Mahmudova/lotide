const takeUntil = function(array, callback) {
  let newArr = [];
  for (let i of array) {
    if (callback(i)) {
      return newArr;
    }
    newArr.push(i);
  }
  return newArr;
};


const data1 = [1, 2, 5, 7, 2, -1, 2, 4, 5];
const results1 = takeUntil(data1, x => x < 0);
assertArraysEqual(results1, [1, 2, 3, 7, 2]);

